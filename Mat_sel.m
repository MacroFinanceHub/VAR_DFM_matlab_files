function [data1,data2] = Mat_sel(data,thvar,th)
%-------------------------------------------------------------------------%
% Matlab 9.0
% Autor: Roque Montero
% Date: 27/Feb/2017
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
% Description: Choose elements of a matrix given an index variable and
% threshold.
% Inputs:
%   data  : Data (TxN).
%   thvar : Threshold variable (Tx1).
%   th    : Threshold value for thvar (1x1).
%
% Outputs:
%   data1 : Data for variables below or equal to Threshold.
%   data2 : Data for variables greater than Threshold.
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
data1 = data(le(thvar,th),:);
data2 = data(gt(thvar,th),:);
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------