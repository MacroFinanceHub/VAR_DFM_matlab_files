function z = trimr(x,n1,n2)
%-------------------------------------------------------------------------%
% Matlab 9.0
% Autor: Roque Montero
% Date: 17/Dec/2016
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
% Description: Return matrix/vector stripped of specified rows (n1,n2).
% Inputs:
% 	x  : input matrix (or vector) (n x k)
%   n1 : first n1 rows to strip
%   n2 : last  n2 rows to strip
% 
% Outputs
% 	z  : Stripped matrix.
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
n = size(x);
if (n1+n2) >= n
    error('Attempting to trim too much in trimr');
end
h1 = n1+1;   
h2 = n-n2;
z = x(h1:h2,:);
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%