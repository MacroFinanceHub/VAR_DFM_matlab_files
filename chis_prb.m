function prob = chis_prb(x,v)
%-------------------------------------------------------------------------%
% Matlab 9.0
% Autor: Roque Montero
% Date: 17/Dec/2016
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
% Description: Computes the chi-squared probability function.
% Inputs:
%   x    : Random variable.
%   v    : Degrees of freedom.
%
% Outputs:
%   prob : Prob of observing a chi-squared value <= x, i.e., prob(x | v).                  
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
if nargin ~= 2
    error('Wrong # of arguments to chis_prb');
end
prob = gammainc(x/2, v/2);
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%