function [row,col,aux] = minMaxMat(MAT,opt)
%-------------------------------------------------------------------------%
% Matlab 9.0
% Autor: Roque Montero
% Date: 17/Dec/2016
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
% Description: Finds min/Max element of a Matrix.
% Inputs:
%   MAT  : Matrix of data.
%   opt  : Option for searching: min(1) o max(2),
%
% Outputs:
%   row  : Row id.
%   col  : Col id.
%   aux  : Min/Max of the matrix.
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
% Setup.
aux = MAT(1,1,1);
row = 1;
col = 1;

% Searching the min(MAT).
if opt == 1
    for i0 =1:size(MAT,1)
        for i1 = 1:size(MAT,2)
            if MAT(i0,i1,1) < aux
                aux = MAT(i0,i1,1);
                row = i0; col = i1;
            end            
        end
    end

% Searching the Max(MAT).
elseif opt == 2
    for i0 =1:size(MAT,1)
        for i1 = 1:size(MAT,2)
            if MAT(i0,i1,1) > aux
                aux = MAT(i0,i1,1);
                row = i0; col = i1;
            end            
        end
    end
end
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%
%-------------------------------------------------------------------------%